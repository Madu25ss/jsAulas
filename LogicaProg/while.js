//while: executa enquanto verdadeiro, verifica e executa

let n = 0;
while (n<=5) {
    console.log(`Valor de n = ${n}`);
    n++; //loop sem isso aki 
};

//do while: executa enquanto verdadeiro, executa e depois verifica
console.log(`####Do while`)

let x = 0;
do {
    console.log(`valor de x = ${x}`);
    x++
} while (x==0);